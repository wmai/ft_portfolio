#include "inc/fdf.h"

void				fdf_fill_img(t_env *e)
{
	int				x;
	int				y;

	x = 0;
	y = 0;
	while (x * y < WIN_W * WIN_H)
	{
		fdf_putpixel(e, x, y, 0x000000);
		if (x > WIN_W)
		{
			y++;
			x = -1;
		}
		x++;
	}
}
