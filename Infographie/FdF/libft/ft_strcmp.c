#include "inc/libft.h"

int			ft_strcmp(const char *s1, const char *s2)
{
	while (*s1++ == *s2++)
	{
		if (!*s1 && !*s2)
			return (0);
	}
	return ((*--s1 < *--s2) ? -1 : 1);
}
