#include "inc/libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	j;
	size_t	m;

	i = 0;
	j = 0;
	m = 0;
	while (s1[i])
		i++;
	while (s2[j] && m < n)
	{
		s1[i + j] = s2[j];
		j++;
		m++;
	}
	s1[i + j] = '\0';
	return (s1);
}
